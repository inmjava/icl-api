package com.copel.picmicroservice.externo.rhj;

public class ProfissionalDTO {
	
	private Long numReg;
	private Long tipo;
	private String nome;
	private String tipoVinc;
	private String codDemissao;
	
	
	public Long getNumReg() {
		return numReg;
	}
	public void setNumReg(Long numReg) {
		this.numReg = numReg;
	}
	public Long getTipo() {
		return tipo;
	}
	public void setTipo(Long tipo) {
		this.tipo = tipo;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getTipoVinc() {
		return tipoVinc;
	}
	public void setTipoVinc(String tipoVinc) {
		this.tipoVinc = tipoVinc;
	}
	public String getCodDemissao() {
		return codDemissao;
	}
	public void setCodDemissao(String codDemissao) {
		this.codDemissao = codDemissao;
	}
	

	
}
